console = "Nintendo Switch"
print("console.count(i) = %d"%console.count("i")) #count : 문자 세기
print("console.find(i) = %d"%console.find("i")) #find : 문자 찾기
print("console.upper = %s"%console.upper()) #upper : 대문자로 바꾸기
print("console.lower = %s"%console.lower()) #lower : 소문자로 바꾸기
print("console.replace(\"Switch\", \"Wii\") = %s"%console.replace("Switch", "Wii")) #replace : 바꾸기